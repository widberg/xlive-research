static unsigned char xtime(unsigned char a)
{
    return (a & 0x80) ? (unsigned char)((a << 1) ^ 0x1b) : (unsigned char)(a << 1);
}

static unsigned char gmul(unsigned char a, unsigned char b)
{
    unsigned char r = 0;
    while (b)
    {
        if (b & 1)
            r ^= a;
        a = xtime(a);
        b >>= 1;
    }
    return r;
}

static void SubBytes(unsigned char *s)
{
    for (int i = 0; i < 16; ++i)
        s[i] = SBOX[s[i]];
}

static void InvSubBytes(unsigned char *s)
{
    for (int i = 0; i < 16; ++i)
        s[i] = INV_SBOX[s[i]];
}

static void ShiftRows(unsigned char *s)
{
    unsigned char t[16];
    for (int i = 0; i < 16; ++i)
        t[i] = s[(i % 4) + 4 * (((i / 4) + (i % 4)) % 4)];
    memcpy(s, t, 16);
}

static void InvShiftRows(unsigned char *s)
{
    unsigned char t[16];
    for (int i = 0; i < 16; ++i)
        t[i] = s[(i % 4) + 4 * ((((i / 4) - (i % 4)) % 4 + 4) % 4)];
    memcpy(s, t, 16);
}

static void MixColumns(unsigned char *s)
{
    unsigned char o[16];
    for (int c = 0; c < 4; ++c)
    {
        const unsigned char *col = s + 4 * c;
        o[4 * c + 0] = gmul(2, col[0]) ^ gmul(3, col[1]) ^ gmul(1, col[2]) ^ gmul(1, col[3]);
        o[4 * c + 1] = gmul(1, col[0]) ^ gmul(2, col[1]) ^ gmul(3, col[2]) ^ gmul(1, col[3]);
        o[4 * c + 2] = gmul(1, col[0]) ^ gmul(1, col[1]) ^ gmul(2, col[2]) ^ gmul(3, col[3]);
        o[4 * c + 3] = gmul(3, col[0]) ^ gmul(1, col[1]) ^ gmul(1, col[2]) ^ gmul(2, col[3]);
    }
    memcpy(s, o, 16);
}

static void InvMixColumns(unsigned char *s)
{
    unsigned char o[16];
    for (int c = 0; c < 4; ++c)
    {
        const unsigned char *col = s + 4 * c;
        o[4 * c + 0] = gmul(14, col[0]) ^ gmul(11, col[1]) ^ gmul(13, col[2]) ^ gmul(9, col[3]);
        o[4 * c + 1] = gmul(9, col[0]) ^ gmul(14, col[1]) ^ gmul(11, col[2]) ^ gmul(13, col[3]);
        o[4 * c + 2] = gmul(13, col[0]) ^ gmul(9, col[1]) ^ gmul(14, col[2]) ^ gmul(11, col[3]);
        o[4 * c + 3] = gmul(11, col[0]) ^ gmul(13, col[1]) ^ gmul(9, col[2]) ^ gmul(14, col[3]);
    }
    memcpy(s, o, 16);
}

static void AddRoundKey(unsigned char *s, const unsigned char *rk)
{
    for (int i = 0; i < 16; ++i)
        s[i] ^= rk[i];
}

// W = (nr + 1) round keys (16 bytes each). The final round omits MixColumns.
static void AesEncryptBlock(const unsigned char in[16], unsigned char out[16],
                            const unsigned char *W, int nr)
{
    unsigned char s[16];
    memcpy(s, in, 16);
    AddRoundKey(s, W);
    for (int r = 1; r < nr; ++r)
    {
        SubBytes(s);
        ShiftRows(s);
        MixColumns(s);
        AddRoundKey(s, W + 16 * r);
    }
    SubBytes(s);
    ShiftRows(s);
    AddRoundKey(s, W + 16 * nr);
    memcpy(out, s, 16);
}

static void AesDecryptBlock(const unsigned char in[16], unsigned char out[16],
                            const unsigned char *W, int nr)
{
    unsigned char s[16];
    memcpy(s, in, 16);
    AddRoundKey(s, W + 16 * nr);
    InvShiftRows(s);
    InvSubBytes(s);
    for (int r = nr - 1; r >= 1; --r)
    {
        AddRoundKey(s, W + 16 * r);
        InvMixColumns(s);
        InvShiftRows(s);
        InvSubBytes(s);
    }
    AddRoundKey(s, W);
    memcpy(out, s, 16);
}

// Standard AES-128 key schedule: expands a 16-byte master key into 11 round
// keys (176 bytes). constexpr so the system_link schedule is computed once at
// compile time rather than on every block call.
static constexpr std::array<unsigned char, 11 * 16> Aes128Schedule(const unsigned char (&key)[16])
{
    constexpr unsigned char RC[11] = { 0, 1, 2, 4, 8, 16, 32, 64, 128, 0x1b, 0x36 };
    std::array<unsigned char, 11 * 16> W{};
    for (int i = 0; i < 16; ++i) W[i] = key[i];
    for (int r = 1; r < 11; ++r)
    {
        int prev = 16 * (r - 1);
        int cur = 16 * r;
        unsigned char t[4] = {};
        // RotWord(prev column 3) then SubWord, then xor round constant.
        t[0] = SBOX[W[prev + 13]] ^ RC[r];
        t[1] = SBOX[W[prev + 14]];
        t[2] = SBOX[W[prev + 15]];
        t[3] = SBOX[W[prev + 12]];
        for (int i = 0; i < 4; ++i) W[cur + 0 + i] = W[prev + 0 + i] ^ t[i];
        for (int i = 0; i < 4; ++i) W[cur + 4 + i] = W[prev + 4 + i] ^ W[cur + 0 + i];
        for (int i = 0; i < 4; ++i) W[cur + 8 + i] = W[prev + 8 + i] ^ W[cur + 4 + i];
        for (int i = 0; i < 4; ++i) W[cur + 12 + i] = W[prev + 12 + i] ^ W[cur + 8 + i];
    }
    return W;
}

