// Expanded at compile time; system_link blocks just index into this.
static constexpr std::array<unsigned char, 11 * 16> SYSTEM_LINK_SCHEDULE =
    Aes128Schedule(SYSTEM_LINK_MASTER);

// SBuffer block helpers: copy 16 bytes between an SB_PTR and a stack buffer.

static void ReadBlock(SB_PTR<unsigned char> sb, unsigned char out[16])
{
    for (unsigned long i = 0; i < 16; ++i)
        out[i] = SBufferGetByte(sb.ptr, sb.begin + i);
}

static void WriteBlock(SB_PTR<unsigned char> sb, const unsigned char in[16])
{
    for (unsigned long i = 0; i < 16; ++i)
        SBufferSetByte(sb.ptr, sb.begin + i, in[i]);
}

// Block entry points

HRESULT ObfuscationEncryptBlock(SB_PTR<unsigned char> plain, SB_PTR<unsigned char> cypher, unsigned long sixteen)
{
    if ( sixteen != 16 )
        return SL_REMAPPING_SP_PUB_CRYPTO_INVALID_BLOCKLENGTH;
    unsigned char in[16], out[16];
    ReadBlock(plain, in);
    AesEncryptBlock(in, out, &OBFUSCATION_KEYS[0][0], 11);
    WriteBlock(cypher, out);
    return 0;
}

